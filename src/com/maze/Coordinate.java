package com.maze;

/* The Coordinates helps to maps the maze
* and defines the coordinates of the mazeSolver
* */
public class Coordinate {

    private int x, y;

    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
